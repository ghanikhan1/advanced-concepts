<?php

namespace App\Libraries;

Class Math {

    public static function add($num1, $num2)
    {
        return $num1 + $num2;
    }

    public static function subtract($num1, $num2)
    {
        return $num1 - $num2;
    }

    public static function multiply($num1, $num2)
    {
        return $num1 * $num2;
    }

}
